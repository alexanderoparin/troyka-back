package ru.oparin.troyka.model.enums;

/**
 * Перечисление статусов платежей в системе.
 * Определяет различные состояния платежа в процессе его обработки
 * от создания до завершения или отмены.
 */
public enum PaymentStatus {
    
    /**
     * Платеж создан.
     * Начальное состояние платежа после его создания в системе.
     */
    CREATED,
    
    /**
     * Ожидает оплаты.
     * Платеж создан и ожидает оплаты пользователем.
     */
    PENDING,
    
    /**
     * Оплачен.
     * Платеж успешно завершен, средства получены.
     */
    PAID,
    
    /**
     * Неудачный.
     * Платеж не удалось обработать из-за ошибки.
     */
    FAILED,
    
    /**
     * Отменен.
     * Платеж был отменен пользователем или системой.
     */
    CANCELLED,
    
    /**
     * Возвращен.
     * Платеж был возвращен пользователю.
     */
    REFUNDED
}
